    void warn() {
        if (!jTextField1.getText().isEmpty()) {
            try {
                if (Integer.parseInt(jTextField1.getText()) <= 100) {
                    jTextField2.setText(Utils.convert(Integer.parseInt(jTextField1.getText())));
                }
            } catch (NumberFormatException ex) {
            }
        }
    }

    void refresh() {
        jComboBox1.setModel(new StandarKompetensiCombo());
        jComboBox1.setSelectedIndex(0);

        db.Query q = new db.Query("nilai");
        q.select("kode_nilai").select("nilai.kode_sk").select("nama_sk").select("nilai_angka").select("nilai_huruf");
        q.join("standar_kompetensi", "standar_kompetensi.kode_sk = nilai.kode_sk", "");
        q.where("kode_guru", Session.CURRENT_USER.get("kode_guru").toString());
        q.where("nisn", this.mSiswa.get("nisn").toString());

        List<db.Model> models = db.query(q);
        NilaiSiswaTable table = new NilaiSiswaTable(models);

        jTable1.setModel(table);

        try {
            jTextField4.setText(db.query(new db.Query("nilai").max("nilai_angka").where("nisn", this.mSiswa.get("nisn").toString()).where("kode_guru", Session.CURRENT_USER.get("kode_guru").toString())).get(0).get("MAX(nilai_angka)").toString());
            jTextField5.setText(db.query(new db.Query("nilai").min("nilai_angka").where("nisn", this.mSiswa.get("nisn").toString()).where("kode_guru", Session.CURRENT_USER.get("kode_guru").toString())).get(0).get("MIN(nilai_angka)").toString());
            jTextField6.setText(db.query(new db.Query("nilai").avg("nilai_angka").where("nisn", this.mSiswa.get("nisn").toString()).where("kode_guru", Session.CURRENT_USER.get("kode_guru").toString())).get(0).get("AVG(nilai_angka)").toString());
        } catch (NullPointerException ex) {

        }
    }

    void refresh(db.Query q) {
        List<db.Model> models = db.query(q);
        NilaiSiswaTable table = new NilaiSiswaTable(models);

        jTable1.setModel(table);
    }

    private void clean() {
        jTextField7.setText("");
        jTextField1.setText("");
        jTextField2.setText("");
    }

    private void setEditable(boolean bool) {
        if (!bool) {
            jButton1.setText("Insert");
            jButton5.setText("Reset");

            jTextField2.setEditable(false);
            jComboBox1.setEnabled(false);

            isInEditable = bool;
            jButton2.setEnabled(true);
        } else {
            jTextField1.setEditable(true);
            jComboBox1.setEnabled(true);

            jTextField1.requestFocus();

            isInEditable = true;

            jButton1.setText("Save");
            jButton5.setText("Cancel");

            jButton2.setEnabled(false);
        }
    }

