    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!isInEditable) {
            setEditable(true);
            this.mode = 0;
        } else {
            int result = -1;
            if (mode == 0) {
                result = NilaiModel.insert(this.mSiswa.get("nisn").toString(), Session.CURRENT_USER.get("kode_guru").toString(), jComboBox1.getSelectedItem().toString().split(" ")[0], jTextField1.getText(), jTextField2.getText());
            } else {
                result = NilaiModel.update(jTextField7.getText(), this.mSiswa.get("nisn").toString(), Session.CURRENT_USER.get("kode_guru").toString(), jComboBox1.getSelectedItem().toString().split(" ")[0], jTextField1.getText(), jTextField2.getText());
            }
            if (result == 1) {
                refresh();
                clean();
                JOptionPane.showMessageDialog(this, "Success", "OK", JOptionPane.INFORMATION_MESSAGE);
                setEditable(false);
            } else {
                JOptionPane.showMessageDialog(this, "Failed", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int selected = jTable1.getSelectedRow();

        if (selected >= 0) {
            int result = JOptionPane.showConfirmDialog(this, "Are you sure?", "Delete", JOptionPane.OK_CANCEL_OPTION);
            if (result == 0) {
                int result2 = NilaiModel.delete(jTable1.getValueAt(selected, 0).toString());
                if (result2 == 1) {
                    refresh();

                    JOptionPane.showMessageDialog(this, "Success", "OK", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Failed", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

